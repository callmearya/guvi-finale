function populateStates(preserveSelection = true) {
  const select = document.getElementById('state-select');
  if (!select) return;
  const previousValue = preserveSelection ? select.value : '';
  select.innerHTML = '';

  const placeholder = document.createElement('option');
  placeholder.value = '';
  placeholder.textContent = stringsForLanguage(currentLanguage()).statePlaceholder;
  select.appendChild(placeholder);

  stateOptions.forEach((item) => {
    const option = document.createElement('option');
    option.value = item;
    option.textContent = item;
    select.appendChild(option);
  });

  const target = state.farmerState || previousValue;
  if (target && stateOptions.includes(target)) {
    select.value = target;
  }
}

function populateDistricts(selectedState, preserveSelection = true) {
  const select = document.getElementById('district-select');
  if (!select) return;
  const previousValue = preserveSelection ? select.value : '';
  select.innerHTML = '';

  const placeholder = document.createElement('option');
  placeholder.value = '';
  placeholder.textContent = stringsForLanguage(currentLanguage()).districtPlaceholder;
  select.appendChild(placeholder);

  const districts = stateDistrictMap[selectedState] || [];
  districts.forEach((item) => {
    const option = document.createElement('option');
    option.value = item;
    option.textContent = item;
    select.appendChild(option);
  });

  const target = preserveSelection ? previousValue : null;
  const candidate = target || state.district;
  if (candidate && districts.includes(candidate)) {
    select.value = candidate;
  }
}

function goToStep(index) {
  state.stepIndex = index;
  const steps = document.querySelectorAll('.wizard-step');
  steps.forEach((step, idx) => {
    step.hidden = idx !== index;
  });
  const strings = I18N[state.language || 'en'];
  const titles = [
    strings.chooseLanguage,
    strings.chooseCommodity,
    strings.chooseMarket,
    strings.chooseQuantity,
    strings.locationHelper,
  ];
  document.getElementById('wizard-title').textContent = titles[index] || strings.chooseLanguage;
  updateFooterButtons();
}

function updateFooterButtons() {
  const backBtn = document.getElementById('back-button');
  const nextBtn = document.getElementById('next-button');
  const submitBtn = document.getElementById('submit-button');

  backBtn.hidden = state.stepIndex === 0;

  if (state.stepIndex === 4) {
    nextBtn.hidden = true;
    submitBtn.hidden = false;
  } else if (state.stepIndex === 3) {
    nextBtn.hidden = false;
    submitBtn.hidden = true;
    nextBtn.disabled = false;
  } else if (state.stepIndex === 2) {
    nextBtn.hidden = false;
    submitBtn.hidden = true;
    nextBtn.disabled = !state.market;
  } else if (state.stepIndex === 1) {
    nextBtn.hidden = false;
    submitBtn.hidden = true;
    nextBtn.disabled = !state.commodity;
  } else {
    nextBtn.hidden = true;
    submitBtn.hidden = true;
  }
}

