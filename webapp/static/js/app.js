const translationCache = new Map();

function currentLanguage() {
  if (state.language) {
    return state.language;
  }
  const config = window.APP_CONFIG || {};
  return config.defaultLanguage || 'en';
}

function stringsForLanguage(lang) {
  return I18N[lang] || I18N.en;
}

function quantitySuffix(lang) {
  const target = lang || currentLanguage();
  return stringsForLanguage(target).quantityUnit || I18N.en.quantityUnit;
}

function horizonSuffix(lang) {
  const target = lang || currentLanguage();
  return stringsForLanguage(target).horizonUnit || I18N.en.horizonUnit;
}

function formatQuantityLabel(value) {
  return `${value} ${quantitySuffix()}`;
}

function formatHorizonLabel(days) {
  return `${days} ${horizonSuffix()}`;
}

async function translateTexts(texts, language) {
  if (!language || language === 'en') {
    return {};
  }
  const unique = [...new Set((texts || []).filter((text) => text))];
  if (!unique.length) {
    return {};
  }
  const cacheKey = `${language}:${unique.join('|')}`;
  if (translationCache.has(cacheKey)) {
    return translationCache.get(cacheKey);
  }
  const response = await fetch('/api/translate', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ texts: unique, language }),
  });
  if (!response.ok) {
    return {};
  }
  const data = await response.json();
  const map = {};
  unique.forEach((text, idx) => {
    map[text] = data.translations[idx] || text;
  });
  translationCache.set(cacheKey, map);
  return map;
}

function buildContextLines(data) {
  const lines = [];
  const transport = data.transport || {};
  const supply = data.supply_demand || {};
  if (transport.per_quintal_cost) {
    const distance = typeof transport.distance_km === 'number' ? transport.distance_km.toFixed(0) : transport.distance_km || '';
    lines.push(`Transport: ₹${transport.per_quintal_cost.toFixed(0)} / ${quantitySuffix()} over ${distance} km`);
  }
  if (supply.supply_pressure !== undefined && supply.supply_pressure !== null) {
    lines.push(`Supply pressure indicator: ${(supply.supply_pressure * 100).toFixed(1)}%`);
  }
  if (supply.latest_arrivals !== undefined && supply.latest_arrivals !== null) {
    lines.push(`Latest arrivals reported: ${supply.latest_arrivals.toFixed(0)} ${quantitySuffix()}`);
  }
  return lines;
}

function formatBestWindow(bestWindow) {
  if (!bestWindow || !bestWindow.date || bestWindow.price === null) {
    return '';
  }
  return `Optimal sale window: around ${bestWindow.date} (forecast ₹${bestWindow.price} / ${quantitySuffix()})`;
}

